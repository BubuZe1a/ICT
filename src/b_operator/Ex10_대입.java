package b_operator;

public class Ex10_대입 {

	public static void main(String[] args) {
		
		int a = 10;
		int b = 7;
		
		//a = a + b ;
		a += b;
		System.out.println("결과:" + a); // 17

		
		a -= b;
		System.out.println("결과:" + a); // 10
		
		a *= b;
		System.out.println("결과:" + a); // 70
		
		a %= b; // 나머지를 구하는 나누기
		System.out.println("결과:" + a); // 
		
		a /= b; // 몫을 구하는 나누기
		System.out.println("결과:" + a); //
		
		
		
		
		
		
		
		
		
	}

}
